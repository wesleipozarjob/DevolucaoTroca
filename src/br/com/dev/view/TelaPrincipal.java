    private Boolean ehProdutoApto(String codigo, String desc, String qtd, String valor) {
        if (codigo.isEmpty() || desc.isEmpty() || qtd.isEmpty() || valor.isEmpty()) {
            return Boolean.FALSE;
        }
        return Boolean.TRUE;
    }

    private Produto gerarProduto(String tipo, String codigo, String desc, String qtd, String valor) {
        Produto produto;
        if (tipo.equals("PESO")) {
            produto = new ProdutoPeso(codigo, desc, Double.parseDouble(valor), Double.parseDouble(qtd));
        } else {
            produto = new ProdutoQuantidade(codigo, desc, Double.parseDouble(valor), Integer.parseInt(qtd));
        }
        return produto;
    }

